#!/usr/bin/env node
// One-shot extractor: reads the law-firm letterhead from `en tête.docx`
// and writes it as an inline HTML constant to src/lib/pdf/letterhead.ts.
// Run with: npm run build:letterhead

import mammoth from "mammoth";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const sourceDocx = path.join(repoRoot, "en tête.docx");
const outFile = path.join(repoRoot, "src", "lib", "pdf", "letterhead.ts");

async function main() {
  try {
    await fs.access(sourceDocx);
  } catch {
    console.error(`[extract-letterhead] source not found: ${sourceDocx}`);
    process.exit(1);
  }

  const { value: rawHtml, messages } = await mammoth.convertToHtml(
    { path: sourceDocx },
    {
      convertImage: mammoth.images.imgElement(async (image) => {
        const buf = await image.read("base64");
        return { src: `data:${image.contentType};base64,${buf}` };
      }),
    },
  );

  for (const m of messages) {
    if (m.type === "error") console.error(`[extract-letterhead] ${m.message}`);
  }

  // Light cleanup: trim, collapse multiple blank lines, strip empty paragraphs.
  const html = rawHtml
    .replace(/<p>\s*<\/p>/g, "")
    .replace(/\r\n/g, "\n")
    .trim();

  // Escape for embedding inside a TS template literal.
  const escaped = html.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

  const ts = `// GENERATED by scripts/extract-letterhead.mjs from \`en tête.docx\` on ${new Date().toISOString()}.
// Do not edit by hand — re-run \`npm run build:letterhead\` to update.

export const LETTERHEAD_HTML = \`${escaped}\`;
`;

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, ts, "utf8");

  const sizeKb = Buffer.byteLength(ts, "utf8") / 1024;
  console.log(
    `[extract-letterhead] wrote ${path.relative(repoRoot, outFile)} (${sizeKb.toFixed(1)} KB)`,
  );
  if (sizeKb > 500) {
    console.warn(
      `[extract-letterhead] WARNING: output is large (${sizeKb.toFixed(1)} KB). Consider downscaling logo images in en tête.docx.`,
    );
  }
}

main().catch((err) => {
  console.error("[extract-letterhead] failed:", err);
  process.exit(1);
});
