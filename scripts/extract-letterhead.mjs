#!/usr/bin/env node
// One-shot extractor: reads the law-firm letterhead from `en tête.docx`
// and writes it as an inline HTML constant to src/lib/pdf/letterhead.ts.
//
// The docx anchors the QR image absolutely on the left, behind a 2-column
// table of cabinet info + file/date fields. mammoth's HTML output stacks
// the image above the table, which doesn't visually match the docx and
// blows past the PDF top margin. So we rebuild the structure as a
// 3-column table row: [image] [cabinet info, centered] [file/date fields].
//
// Run with: npm run build:letterhead

import mammoth from "mammoth";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const sourceDocx = path.join(repoRoot, "en tête.docx");
const outFile = path.join(repoRoot, "src", "lib", "pdf", "letterhead.ts");

async function main() {
  try {
    await fs.access(sourceDocx);
  } catch {
    console.error(`[extract-letterhead] source not found: ${sourceDocx}`);
    process.exit(1);
  }

  const { value: rawHtml, messages } = await mammoth.convertToHtml(
    { path: sourceDocx },
    {
      convertImage: mammoth.images.imgElement(async (image) => {
        const buf = await image.read("base64");
        return { src: `data:${image.contentType};base64,${buf}` };
      }),
    },
  );

  for (const m of messages) {
    if (m.type === "error") console.error(`[extract-letterhead] ${m.message}`);
  }

  const cleaned = rawHtml
    .replace(/<p>\s*<\/p>/g, "")
    .replace(/\r\n/g, "\n")
    .trim();

  // Extract the first <img> tag (the QR / cabinet logo).
  const imgMatch = cleaned.match(/<img\b[^>]*\/?>/);
  if (!imgMatch) {
    console.error("[extract-letterhead] no <img> found in extracted HTML");
    process.exit(1);
  }
  const imgTag = imgMatch[0];

  // Extract the two <td> cells from the first table.
  const tdMatches = [...cleaned.matchAll(/<td[^>]*>([\s\S]*?)<\/td>/g)];
  if (tdMatches.length < 2) {
    console.error("[extract-letterhead] expected at least 2 <td> cells in letterhead table");
    process.exit(1);
  }
  const cabinetInfo = tdMatches[0][1].trim();
  const fileFields = tdMatches[1][1].trim();

  // Build a 3-column table. Inline styles only — this HTML is embedded in
  // both the in-app preview and the Puppeteer PDF, neither of which apply
  // surrounding stylesheets to its <table>.
  const structured = `<table dir="rtl" style="width:100%;border-collapse:collapse;table-layout:fixed;direction:rtl;margin:0 0 12px;"><tr style="vertical-align:top;"><td style="width:22%;padding:0 8px 0 0;text-align:center;"><img-placeholder/></td><td style="width:53%;text-align:center;padding:0 4px;">${cabinetInfo}</td><td style="width:25%;text-align:right;padding:0 0 0 4px;">${fileFields}</td></tr></table>`;

  const styledImg = imgTag.replace(
    /<img\b/,
    '<img style="max-width:100%;height:auto;display:inline-block;"'
  );

  const finalHtml = structured.replace("<img-placeholder/>", styledImg);

  // Escape for embedding inside a TS template literal.
  const escaped = finalHtml
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");

  const ts = `// GENERATED by scripts/extract-letterhead.mjs from \`en tête.docx\` on ${new Date().toISOString()}.
// Do not edit by hand — re-run \`npm run build:letterhead\` to update.

export const LETTERHEAD_HTML = \`${escaped}\`;
`;

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, ts, "utf8");

  const sizeKb = Buffer.byteLength(ts, "utf8") / 1024;
  console.log(
    `[extract-letterhead] wrote ${path.relative(repoRoot, outFile)} (${sizeKb.toFixed(1)} KB)`,
  );
  if (sizeKb > 500) {
    console.warn(
      `[extract-letterhead] WARNING: output is large (${sizeKb.toFixed(1)} KB). Consider downscaling logo images in en tête.docx.`,
    );
  }
}

main().catch((err) => {
  console.error("[extract-letterhead] failed:", err);
  process.exit(1);
});
